    private void StartTimer(){
        UpdateTime();
        UpdateLabel();
        
    }
    
    private void UpdateTime(){
        segundos++;
        
        if(segundos == 60){
            segundos = 0;
            minutos++;
        }
        if(minutos == 60){
            minutos = 0;
            horas++;
        }
        if(horas == 24){
            horas = 0;
            dias++;
        }
    }
    
    private void UpdateLabel(){
        String cronometro = dias + "d:" + horas + "h:" + minutos + "m:" + segundos + "s:";
        TimerLab.setText(cronometro);
    }

